public class SwitchChar {
    public static void main(String[] args) {
        char sinal = "X";
        switch (sinal) {
            case "x":
            case "X":
            case "*":
            case ".":
                System.out.println("multiplicação");break;
            case "+":
                System.out.println("Adição");break;
            case "-":
                System.out.println("Subtração");break;
        }
    }
}